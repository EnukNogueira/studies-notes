"""Operadores lógicos e o tipo boolean
Em ciência da computação, boolean ou lógico é um tipo de dado primitivo que possui dois valores: verdadeiro (True) ou falso (False).
Sempre usar a primeira letra maiúscula para reconhecer como um tipo lógico.
"""

# Operadores lógicos
"""
AND (e)
True and True = True
True and False = False
False and True = False
False and False = False
"""

"""
OR (ou)
True or True = True
True or False = True
False or True = True
False or False = False
"""
